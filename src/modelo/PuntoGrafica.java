package modelo;

public class PuntoGrafica {

    private int id;
    private int idEcuacion;
    private double x;
    private double y;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdEcuacion() {
        return idEcuacion;
    }

    public void setIdEcuacion(int idEcuacion) {
        this.idEcuacion = idEcuacion;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }   
}
